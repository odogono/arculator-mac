static inline int preset_from_display_name(const char *name)
{
	for (int c = 0; presets[c].name[0]; c++)
	{
		if (!strcmp(presets[c].name, name))
			return c;
	}
	return 0;
}

static inline int preset_from_config_name(const char *name)
{
	for (int c = 0; presets[c].name[0]; c++)
	{
		if (!strcmp(presets[c].config_name, name))
			return c;
	}
	return 0;
}

static inline int index_for_name(const char *const *names, int count, const char *name)
{
	for (int c = 0; c < count; c++)
	{
		if (!strcmp(names[c], name))
			return c;
	}
	return 0;
}

static inline int preset_count(void)
{
	int c = 0;
	while (presets[c].name[0])
		c++;
	return c;
}

static inline int preset_allowed_by_rom(int preset)
{
	return (romset_available_mask & presets[preset].allowed_romset_mask) != 0;
}

